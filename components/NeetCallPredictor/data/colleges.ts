export type Category = "ur" | "ews" | "obc" | "sc" | "st";

export type CollegeType =
  | "AIIMS"
  | "JIPMER"
  | "CENTRAL"
  | "GOVT"
  | "GMERS"
  | "AIDED"
  | "ESIC"
  | "PRIVATE"
  | "DEEMED";

export interface CategoryAir {
  ur?: number;
  ews?: number;
  obc?: number;
  sc?: number;
  st?: number;
}

export interface College {
  /** Canonical college name. Used for fuzzy-matching to college_microsites.college_name. */
  name: string;
  /** Indian state where the college is physically located. */
  state: string;
  city?: string;
  type: CollegeType;
  /** Annual tuition fee, human-readable. */
  fees?: string;
  /** AIQ (15% MCC pool) Round-3 closing AIRs by category. Open to all domiciles. */
  aiq?: CategoryAir;
  /** State-quota (85%) closing AIRs by category. Only applies to domicile-state students. */
  state_quota?: CategoryAir;
}

